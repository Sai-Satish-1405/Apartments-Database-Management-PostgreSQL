CREATE SEQUENCE lease_file_file_id_seq START WITH 101 INCREMENT BY 1 MINVALUE 101 MAXVALUE 999 CACHE 1; 

CREATE SEQUENCE lease_file_lease_number_seq START WITH 100001 INCREMENT BY 10 MINVALUE 100001 MAXVALUE 999999 CACHE 1; 

CREATE TABLE public.lease_file (
    file_id INTEGER PRIMARY KEY DEFAULT nextval('lease_file_file_id_seq'),
    property_id INT UNIQUE NOT NULL,
    lease_number INTEGER NOT NULL DEFAULT nextval('lease_file_lease_number_seq'),
    lease_start_date DATE NOT NULL,
    lease_end_date DATE NOT NULL,
    lease_document BYTEA NOT NULL,
    rent_amount INT,
    uploaded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_lease_file_property FOREIGN KEY (property_id) 
        REFERENCES public.property (property_id) 
        ON DELETE CASCADE
);
